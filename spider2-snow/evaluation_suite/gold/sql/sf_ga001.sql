"purchase_items" AS (
  SELECT
    ("ECOMMERCE":"transaction_id")::string AS "TRANSACTION_ID",
    f.value:"item_id"::string AS "ITEM_ID",
    f.value:"item_name"::string AS "ITEM_NAME",
    COALESCE(f.value:"quantity"::number, 1) AS "QUANTITY"
  FROM "events_dec" e,
  LATERAL FLATTEN(input => e."ITEMS") f
  WHERE e."EVENT_NAME" = 'purchase'
    AND ("ECOMMERCE":"transaction_id") IS NOT NULL
    AND ("ECOMMERCE":"transaction_id")::string != '(not set)'
),
"tx_with_target" AS (
  SELECT DISTINCT "TRANSACTION_ID"
  FROM "purchase_items"
  WHERE UPPER("ITEM_NAME") = 'GOOGLE NAVY SPECKLED TEE'
)
SELECT
  p."ITEM_NAME" AS "OTHER_PRODUCT_NAME",
  SUM(p."QUANTITY") AS "TOTAL_QUANTITY_ALONGSIDE"
FROM "purchase_items" p
JOIN "tx_with_target" t
  ON p."TRANSACTION_ID" = t."TRANSACTION_ID"
WHERE UPPER(p."ITEM_NAME") != 'GOOGLE NAVY SPECKLED TEE'
GROUP BY p."ITEM_NAME"
ORDER BY SUM(p."QUANTITY") DESC, p."ITEM_NAME" ASC
LIMIT 1;